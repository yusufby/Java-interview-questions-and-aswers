public class AreaAndParameterOfCircle {
    public static void main(String[] args) {
        double radius=5.5 ;
        double π=3.14;

        double perimeter = 2 * radius * π;
        double area = radius * radius * π;

        System.out.println("perimeter is " + perimeter);
        System.out.println("area is " + area);
    }
}
